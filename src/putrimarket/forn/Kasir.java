    private void cariBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariBtnActionPerformed
        if (inputCariKasir.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Tidak Dapat Mencari Data. Kamu Belum Mengisi Kata Kunci!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        String sqlCari = "SELECT * FROM kasir WHERE id_kasir LIKE ? OR nama_kasir LIKE ?";
        try (PreparedStatement ps = conn.prepareStatement(sqlCari)) {
            String keyword = "%" + inputCariKasir.getText() + "%";
            ps.setString(1, keyword);
            ps.setString(2, keyword);

            ResultSet rs = ps.executeQuery();
            tableModel = (DefaultTableModel) table.getModel();
            tableModel.setRowCount(0);

            boolean found = false;
            while (rs.next()) {
                found = true;
                Object[] row = new Object[]{
                    rs.getString("id_kasir"),
                    rs.getString("nama_kasir"),
                    rs.getString("no_telp"),
                    rs.getString("alamat")
                };
                tableModel.addRow(row);
            }

            if (found) {
                JOptionPane.showMessageDialog(this,
                    "Data Kasir Dengan Kata Kunci : " + inputCariKasir.getText().toUpperCase() + " Berhasil Ditemukan!",
                    "Sukses",
                    JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this,
                    "Data Kasir Dengan Kata Kunci : " + inputCariKasir.getText().toUpperCase() + " Tidak Ditemukan!",
                    "Informasi",
                    JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Terjadi Kesalahan Saat Mencari Data Kasir!\n" + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_cariBtnActionPerformed

