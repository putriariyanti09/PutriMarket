    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        if (inputIdKasir.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Tidak Dapat Menyimpan Data. Kamu Belum Mengisi ID Kasir!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (inputNamaKasir.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Tidak Dapat Menyimpan Data. Kamu Belum Mengisi Nama Kasir!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (inputNoTelpKasir.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Tidak Dapat Menyimpan Data. Kamu Belum Mengisi No. Telp Kasir!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (inputAlamatKasir.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Tidak Dapat Menyimpan Data. Kamu Belum Mengisi Alamat Kasir!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        String sqlCheck = "SELECT * FROM kasir WHERE id_kasir=?";
        String sqlInsert = "INSERT INTO kasir (id_kasir, nama_kasir, no_telp, alamat) VALUES (?, ?, ?, ?)";

        try (PreparedStatement psCheck = conn.prepareStatement(sqlCheck)) {
            psCheck.setString(1, inputIdKasir.getText().toUpperCase());
            ResultSet rs = psCheck.executeQuery();

            if (rs.next()) {
                JOptionPane.showMessageDialog(this,
                    "Data Kasir Dengan ID : " + inputIdKasir.getText().toUpperCase() + " Sudah Terdaftar!",
                    "Informasi",
                    JOptionPane.INFORMATION_MESSAGE);
            } else {
                try (PreparedStatement psInsert = conn.prepareStatement(sqlInsert)) {
                    psInsert.setString(1, inputIdKasir.getText().toUpperCase());
                    psInsert.setString(2, inputNamaKasir.getText());
                    psInsert.setString(3, inputNoTelpKasir.getText());
                    psInsert.setString(4, inputAlamatKasir.getText());

                    psInsert.executeUpdate();
                    loadDataTable();
                    JOptionPane.showMessageDialog(this,
                        "Data Kasir Dengan ID : " + inputIdKasir.getText().toUpperCase() + " Berhasil Disimpan!",
                        "Sukses",
                        JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Terjadi Kesalahan Saat Menyimpan Data Kasir!\n" + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_saveBtnActionPerformed

    private void ubahBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahBtnActionPerformed
        if (inputIdKasir.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Tidak Dapat Mengubah Data. Kamu Belum Mengisi ID Kasir!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        String sqlCheck = "SELECT * FROM kasir WHERE id_kasir=?";
        String sqlUpdate = "UPDATE kasir SET nama_kasir=?, no_telp=?, alamat=? WHERE id_kasir=?";

        try (PreparedStatement psCheck = conn.prepareStatement(sqlCheck)) {
            psCheck.setString(1, inputIdKasir.getText().toUpperCase());
            ResultSet rs = psCheck.executeQuery();

            if (rs.next()) {
                try (PreparedStatement psUpdate = conn.prepareStatement(sqlUpdate)) {
                    psUpdate.setString(1, inputNamaKasir.getText().isEmpty() ? rs.getString("nama_kasir") : inputNamaKasir.getText());
                    psUpdate.setString(2, inputNoTelpKasir.getText().isEmpty() ? rs.getString("no_telp") : inputNoTelpKasir.getText());
                    psUpdate.setString(3, inputAlamatKasir.getText().isEmpty() ? rs.getString("alamat") : inputAlamatKasir.getText());
                    psUpdate.setString(4, inputIdKasir.getText().toUpperCase());

                    psUpdate.executeUpdate();
                    loadDataTable();
                    JOptionPane.showMessageDialog(this,
                        "Data Kasir Dengan ID : " + inputIdKasir.getText().toUpperCase() + " Berhasil Diubah!",
                        "Sukses",
                        JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this,
                    "Data Kasir Dengan ID : " + inputIdKasir.getText().toUpperCase() + " Tidak Ditemukan!",
                    "Informasi",
                    JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Terjadi Kesalahan Saat Mengubah Data Kasir!\n" + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_ubahBtnActionPerformed

