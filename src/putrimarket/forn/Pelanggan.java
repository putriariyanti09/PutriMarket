    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        if (inputIdPelanggan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Tidak Dapat Menyimpan Data. Kamu Belum Mengisi ID Pelanggan!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (inputNamaPelanggan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Tidak Dapat Menyimpan Data. Kamu Belum Mengisi Nama Pelanggan!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (getJenisKelamin().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Tidak Dapat Menyimpan Data. Kamu Belum Memilih Jenis Kelamin!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (inputNoTelp.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Tidak Dapat Menyimpan Data. Kamu Belum Mengisi Nomor Telepon!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (inputAlamat.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Tidak Dapat Menyimpan Data. Kamu Belum Mengisi Alamat!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        String sqlCheck = "SELECT * FROM pelanggan WHERE id_pelanggan=?";
        String sqlInsert = "INSERT INTO pelanggan (id_pelanggan, nama_pelanggan, jenis_kelamin, no_telp, alamat) VALUES (?, ?, ?, ?, ?)";

        try (PreparedStatement psCheck = conn.prepareStatement(sqlCheck)) {
            psCheck.setString(1, inputIdPelanggan.getText().toUpperCase());
            ResultSet rs = psCheck.executeQuery();

            if (rs.next()) {
                JOptionPane.showMessageDialog(this,
                        "Data Barang Dengan ID : " + inputIdPelanggan.getText().toUpperCase() + " Sudah Terdaftar!",
                        "Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
            } else {
                try (PreparedStatement psInsert = conn.prepareStatement(sqlInsert)) {
                    psInsert.setString(1, inputIdPelanggan.getText().toUpperCase());
                    psInsert.setString(2, inputNamaPelanggan.getText());
                    psInsert.setString(3, getJenisKelamin());
                    psInsert.setString(4, inputNoTelp.getText());
                    psInsert.setString(5, inputAlamat.getText());

                    psInsert.executeUpdate();
                    loadDataTable();
                    JOptionPane.showMessageDialog(this,
                        "Data Barang Dengan ID : " + inputIdPelanggan.getText().toUpperCase() + " Berhasil Disimpan!",
                        "Sukses",
                        JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Terjadi Kesalahan Saat Menyimpan Data Pelanggan!\n" + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_saveBtnActionPerformed

    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        if (inputIdPelanggan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Tidak Dapat Menyimpan Data. Kamu Belum Mengisi ID Pelanggan!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        String sqlCheck = "SELECT * FROM pelanggan WHERE id_pelanggan=?";
        try (PreparedStatement psCheck = conn.prepareStatement(sqlCheck)) {

            psCheck.setString(1, inputIdPelanggan.getText());
            ResultSet rs = psCheck.executeQuery();

            if (rs.next()) {
                String sqlUpdate = "UPDATE pelanggan SET nama_pelanggan=?, jenis_kelamin=?, no_telp=?, alamat=? WHERE id_pelanggan=?";
                try (PreparedStatement psUpdate = conn.prepareStatement(sqlUpdate)) {
                    psUpdate.setString(1, inputNamaPelanggan.getText().isEmpty() 
                            ? rs.getString("nama_pelanggan") 
                            : inputNamaPelanggan.getText());
                    psUpdate.setString(2, getJenisKelamin().isEmpty() 
                            ? rs.getString("jenis_kelamin") 
                            : getJenisKelamin());
                    psUpdate.setString(3, inputNoTelp.getText().isEmpty() 
                            ? rs.getString("no_telp") 
                            : inputNoTelp.getText());
                    psUpdate.setString(4, inputAlamat.getText().isEmpty() 
                            ? rs.getString("alamat") 
                            : inputAlamat.getText());
                    psUpdate.setString(5, inputIdPelanggan.getText());

                    psUpdate.executeUpdate();
                    loadDataTable();
                    JOptionPane.showMessageDialog(this,
                        "Data Pelanggan Dengan ID : " + inputIdPelanggan.getText().toUpperCase() + " Berhasil Diubah!",
                        "Sukses",
                        JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this,
                        "Data Pelanggan Dengan ID : " + inputIdPelanggan.getText().toUpperCase() + " Tidak Terdaftar!",
                        "Informasi",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Terjadi Kesalahan Saat Mengubah Data Pelanggan!\n" + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_editBtnActionPerformed

