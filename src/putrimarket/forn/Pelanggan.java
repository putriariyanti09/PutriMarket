    private void cariBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariBtnActionPerformed
        if (inputCariPelanggan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Tidak Dapat Mencari Data. Kamu Belum Mengisi Kata Kunci Pencarian!",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        String sqlCari = "SELECT * FROM pelanggan WHERE id_pelanggan LIKE ? OR nama_pelanggan LIKE ?";
        try (PreparedStatement ps = conn.prepareStatement(sqlCari)) {
            String keyword = "%" + inputCariPelanggan.getText() + "%"; 
            ps.setString(1, keyword);
            ps.setString(2, keyword);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                tableModel = (DefaultTableModel) table.getModel();
                tableModel.setRowCount(0);
                do {
                    Object[] row = new Object[]{
                        rs.getString("id_pelanggan"),
                        rs.getString("nama_pelanggan"),
                        rs.getString("jenis_kelamin"),
                        rs.getString("no_telp"),
                        rs.getString("alamat")
                    };
                    tableModel.addRow(row);
                } while (rs.next());

                JOptionPane.showMessageDialog(this,
                    "Data Pelanggan Dengan Kata Kunci : " + inputCariPelanggan.getText().toUpperCase() + " Berhasil Ditemukan!",
                    "Sukses",
                    JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this,
                    "Data Pelanggan Dengan Kata Kunci : " + inputCariPelanggan.getText().toUpperCase() + " Tidak Ditemukan!",
                    "Informasi",
                    JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Terjadi Kesalahan Saat Mencari Data Pelanggan!\n" + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

    }//GEN-LAST:event_cariBtnActionPerformed

